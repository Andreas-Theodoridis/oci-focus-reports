                    log_id = v_log_row_id
                WHERE
                        chat_id = p_chat_id
                    AND log_id IS NULL;

                p_row_id := v_log_row_id;
                p_final_sql := v_response_sql;
                p_final_response := v_response;
                --  Run summarization
                BEGIN
