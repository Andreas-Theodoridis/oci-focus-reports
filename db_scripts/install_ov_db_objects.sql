                    l_input_text := v_summary_prompt
                                    || chr(10)
                                    || chr(10)
                                    || p_final_aimessage;

                    IF l_api_format = 'COHERE' THEN
                        l_payload := '{
                            "compartmentId": "' || p_compartment_id || '",
                            "servingMode": {
