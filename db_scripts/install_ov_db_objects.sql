                            WHEN OTHERS THEN
                                apex_debug.message('‚ùå Failed to extract rephrased text from fallback: %s', SQLERRM);
                                l_result_text := NULL;
                        END;
                    END IF;

                    -- Store result
                    v_reasoned_message := l_result_text;

                    INSERT INTO ai_chat_log_reasoning (
                        chat_id,
                        log_id,
                        input_message,
                        rephrased_output,
