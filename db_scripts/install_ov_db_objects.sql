                        NULL,
                        l_input_text,
                        l_result_text,
                        v_attempt,
                        p_app_user,
                        SYSTIMESTAMP
                    ) RETURNING id INTO v_reasoning_row_id;

                    apex_debug.message('üìù Rephrased: %s', l_result_text);
                END;
            ELSE
                apex_debug.message('‚ùå Rephrasing failed: chatResponse not found');
            END IF;

        ELSE
            EXIT WHEN v_attempt >= v_max_attempts;
        END IF;

    END LOOP;

    IF v_last_failed = 'Y' THEN
        apex_debug.message('üõë Final failure after %s attempts', v_attempt);
        raise_application_error(-20010, 'All AI attempts failed to generate a working SQL query.');
    END IF;

    apex_debug.message('‚úÖ Procedure completed');
END;


